#include <stdio.h>

int main()
{
	float a, b; //оголошення змінних для позначення вхідних даних
	
	printf("a = ");
	scanf_s("%f", &a); //ввід a
	
	printf("b = ");
	scanf_s("%f", &b); //ввід b
	
	float s; //оголошення змінної для позначення площі
	
	s = b * (2 * a - b); //обчислення площі
	printf("s = %f", s); //вивід результату
}