#include <iostream> //п≥дключаЇмо б≥бл≥отеки
#include <cmath>

int main()
{
    double x, eps;
    std::cout << "Enter x\n"; //вводимо значенн€ x
    std::cin >> x;
    std::cout << "Enter eps\n"; //вводимо точн≥сть
    std::cin >> eps;

    int n = 1;
    double y, yn, yn1, zn, zn1, zn2, e;

    zn = sin(x) / (n * n); //шукаЇмо знаменник y(n)  при n=1

    do {
        yn = 1 / zn; // шукаЇмо y(n)
        n = n++; //зб≥льшуЇмо n на 1
        zn2 = sin(x) / (n * n); //шукаЇмо наступний доданок знаменника
        zn1 = zn + zn2; //шукаЇмо знаменник y(n+1)
        yn1 = 1 / zn1; //шукаЇмо y(n+1)
        zn = zn1; //запам'€товуЇмо знаменник y(n+1) €к знаменник y(n)
        std::cout << "\nyn = " << (yn1); //виводимо пром≥жний результат
        e = abs(yn1 - yn); //знаходимо модуль р≥зниц≥ двох останн≥х знайдених y
    } while (e >= eps); //виконуЇмо поки р≥зниц€ двох останн≥х знайдених y не буде меншою за точн≥сть

    y = yn1; //присвоЇнн€ результату зм≥нн≥й y
    std::cout << "\ny = " << (y); //виводимо останнЇ знайдене значенн€ y(n), пораховане з потр≥бною точн≥стю
    return 0;
}
